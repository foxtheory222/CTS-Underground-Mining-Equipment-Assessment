import 'dart:convert';

import 'package:cts_underground_mining_assessment/data/models/inspection_enums.dart';

class SpecValidationIssue {
  const SpecValidationIssue({required this.fieldKey, required this.message});

  final String fieldKey;
  final String message;
}

class SpecValidationResult {
  const SpecValidationResult(this.issues);

  final List<SpecValidationIssue> issues;

  bool get isComplete => issues.isEmpty;
}

class SpecResponse {
  const SpecResponse({
    required this.sectionKey,
    required this.itemKey,
    required this.itemLabel,
    required this.fieldType,
    required this.value,
    required this.isRequired,
    this.conditionRating,
    this.comment,
  });

  final String sectionKey;
  final String itemKey;
  final String itemLabel;
  final InspectionFieldType fieldType;
  final String value;
  final bool isRequired;
  final ConditionRating? conditionRating;
  final String? comment;

  bool get isFlagged => conditionRating?.isFlagged ?? false;

  Map<String, Object?> toJson() => <String, Object?>{
    'sectionKey': sectionKey,
    'itemKey': itemKey,
    'itemLabel': itemLabel,
    'fieldType': fieldType.value,
    'value': value,
    'isRequired': isRequired,
    'conditionRating': conditionRating?.value,
    'comment': comment,
  };

  factory SpecResponse.fromJson(Map<String, Object?> json) {
    return SpecResponse(
      sectionKey: json['sectionKey']! as String,
      itemKey: json['itemKey']! as String,
      itemLabel: json['itemLabel']! as String,
      fieldType: InspectionFieldTypeX.fromValue(json['fieldType']! as String),
      value: json['value']! as String,
      isRequired: json['isRequired']! as bool,
      conditionRating: json['conditionRating'] == null
          ? null
          : ConditionRatingX.fromValue(json['conditionRating']! as String),
      comment: json['comment'] as String?,
    );
  }
}

