class SpecInspection {
  SpecInspection({
    required this.id,
    required this.documentNumber,
    required this.status,
    required this.customer,
    required this.workOrderNumber,
    required this.customerReference,
    required this.assetName,
    required this.siteLocation,
    required this.technicianName,
    required this.servicingShop,
    required this.inspectionDateTime,
    required this.createdAt,
    required this.updatedAt,
    this.completedAt,
    this.emailedAt,
    this.finalTechComments,
    this.signatureFilePath,
    this.generatedPdfPath,
    this.criticalAcknowledged = false,
    List<SpecResponse>? responses,
    List<SpecPhoto>? photos,
    List<SpecActionItem>? actionItems,
    List<SpecHoseEntry>? hoseEntries,
  }) : responses = responses ?? <SpecResponse>[],
       photos = photos ?? <SpecPhoto>[],
       actionItems = actionItems ?? <SpecActionItem>[],
       hoseEntries = hoseEntries ?? <SpecHoseEntry>[];

  final String id;
  String documentNumber;
  InspectionStatus status;
  String customer;
  String workOrderNumber;
  String customerReference;
  String assetName;
  String siteLocation;
  String technicianName;
  String servicingShop;
  DateTime inspectionDateTime;
  DateTime createdAt;
  DateTime updatedAt;
  DateTime? completedAt;
  DateTime? emailedAt;
  String? finalTechComments;
  String? signatureFilePath;
  String? generatedPdfPath;
  bool criticalAcknowledged;
  final List<SpecResponse> responses;
  final List<SpecPhoto> photos;
  final List<SpecActionItem> actionItems;
  final List<SpecHoseEntry> hoseEntries;

  bool get hasProgress =>
      responses.isNotEmpty ||
      photos.isNotEmpty ||
      actionItems.isNotEmpty ||
      hoseEntries.isNotEmpty ||
      (finalTechComments != null && finalTechComments!.trim().isNotEmpty) ||
      signatureFilePath != null;

  Map<String, Object?> toJson() => <String, Object?>{
    'id': id,
    'documentNumber': documentNumber,
    'status': status.value,
    'customer': customer,
    'workOrderNumber': workOrderNumber,
    'customerReference': customerReference,
    'assetName': assetName,
    'siteLocation': siteLocation,
    'technicianName': technicianName,
    'servicingShop': servicingShop,
    'inspectionDateTime': inspectionDateTime.toIso8601String(),
    'createdAt': createdAt.toIso8601String(),
    'updatedAt': updatedAt.toIso8601String(),
    'completedAt': completedAt?.toIso8601String(),
    'emailedAt': emailedAt?.toIso8601String(),
    'finalTechComments': finalTechComments,
    'signatureFilePath': signatureFilePath,
    'generatedPdfPath': generatedPdfPath,
    'criticalAcknowledged': criticalAcknowledged,
    'responses': responses.map((SpecResponse e) => e.toJson()).toList(),
    'photos': photos.map((SpecPhoto e) => e.toJson()).toList(),
    'actionItems': actionItems.map((SpecActionItem e) => e.toJson()).toList(),
    'hoseEntries': hoseEntries.map((SpecHoseEntry e) => e.toJson()).toList(),
  };

  factory SpecInspection.fromJson(Map<String, Object?> json) {
    return SpecInspection(
      id: json['id']! as String,
      documentNumber: json['documentNumber']! as String,
      status: InspectionStatusX.fromValue(json['status']! as String),
      customer: json['customer']! as String,
      workOrderNumber: json['workOrderNumber']! as String,
      customerReference: json['customerReference']! as String,
      assetName: json['assetName']! as String,
      siteLocation: json['siteLocation']! as String,
      technicianName: json['technicianName']! as String,
      servicingShop: json['servicingShop']! as String,
      inspectionDateTime: DateTime.parse(json['inspectionDateTime']! as String),
      createdAt: DateTime.parse(json['createdAt']! as String),
      updatedAt: DateTime.parse(json['updatedAt']! as String),
      completedAt: json['completedAt'] == null
          ? null
          : DateTime.parse(json['completedAt']! as String),
      emailedAt: json['emailedAt'] == null
          ? null
          : DateTime.parse(json['emailedAt']! as String),
      finalTechComments: json['finalTechComments'] as String?,
      signatureFilePath: json['signatureFilePath'] as String?,
      generatedPdfPath: json['generatedPdfPath'] as String?,
      criticalAcknowledged: json['criticalAcknowledged']! as bool,
      responses: (json['responses']! as List<dynamic>)
          .cast<Map<String, Object?>>()
          .map(SpecResponse.fromJson)
          .toList(),
      photos: (json['photos']! as List<dynamic>)
          .cast<Map<String, Object?>>()
          .map(SpecPhoto.fromJson)
          .toList(),
      actionItems: (json['actionItems']! as List<dynamic>)
          .cast<Map<String, Object?>>()
          .map(SpecActionItem.fromJson)
          .toList(),
      hoseEntries: (json['hoseEntries']! as List<dynamic>)
          .cast<Map<String, Object?>>()
          .map(SpecHoseEntry.fromJson)
          .toList(),
    );
  }
}

String encodeSpecJson(Object? value) => jsonEncode(value);

Map<String, Object?> decodeSpecJson(String value) =>
    jsonDecode(value) as Map<String, Object?>;
